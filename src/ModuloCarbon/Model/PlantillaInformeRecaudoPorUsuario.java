
package ModuloCarbon.Model;

public class PlantillaInformeRecaudoPorUsuario {
    private String cedula;
    private String nombre;
    private String zonaOperacion;
    private String recaudo;

    public PlantillaInformeRecaudoPorUsuario() {
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getZonaOperacion() {
        return zonaOperacion;
    }

    public void setZonaOperacion(String zonaOperacion) {
        this.zonaOperacion = zonaOperacion;
    }

    public String getRecaudo() {
        return recaudo;
    }

    public void setRecaudo(String recaudo) {
        this.recaudo = recaudo;
    }
    
    
}
