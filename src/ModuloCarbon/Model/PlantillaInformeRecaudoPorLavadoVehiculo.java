
package ModuloCarbon.Model;

public class PlantillaInformeRecaudoPorLavadoVehiculo {
    private String Zona;
    private String Recaudo;
    private String Debito;
    private String TotalRecaudo;

    public PlantillaInformeRecaudoPorLavadoVehiculo() {
    }

    public String getZona() {
        return Zona;
    }

    public void setZona(String Zona) {
        this.Zona = Zona;
    }

    public String getRecaudo() {
        return Recaudo;
    }

    public void setRecaudo(String Recaudo) {
        this.Recaudo = Recaudo;
    }

    public String getDebito() {
        return Debito;
    }

    public void setDebito(String Debito) {
        this.Debito = Debito;
    }

    public String getTotalRecaudo() {
        return TotalRecaudo;
    }

    public void setTotalRecaudo(String TotalRecaudo) {
        this.TotalRecaudo = TotalRecaudo;
    }
    
    
}
